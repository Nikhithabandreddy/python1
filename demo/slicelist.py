proglang=['c','c++','python','java','php','flutter']
print(proglang)
print(type(proglang))
print("---------------------------")
print("Slicing of list")
print(proglang[:3])#(stop)
print(proglang[3:])#(start:stop)
print(proglang[0:6:1])#(start:stop:stepsize)
print("-----------------------")
print("Negative slicing of list")
print(proglang[:5])
print(proglang[:0])
print(proglang[-1:-5])
print(proglang[-5:-1])
print(proglang[::-1])#reversd list
print(proglang[::1])

